document.addEventListener("DOMContentLoaded", function() {

    marqueeInit({

          uniqueid : "mycrawler1"

        , style : {

             "padding" : "2px"

            , "width" : "100%"

            , "height" : "30px"

        }

        , inc : 2                           // 속도

        , mouse : false       // 마우스 사용여부 "cursor driven"

        , moveatleast : 2                  // 이동속도

        , neutral : 150

        , savedirection : true             // false를 선언하면 마우스 커서가 위치하는 순간 역방향으로 움직인다.

        , random : false                   // 나오는 순서(기본값 : true)

    });

});
